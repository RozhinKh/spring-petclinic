package org.springframework.samples.petclinic.benchmark;

import javax.xml.parsers.DocumentBuilder;
import javax.xml.parsers.DocumentBuilderFactory;
import org.w3c.dom.Document;
import org.w3c.dom.Element;
import org.w3c.dom.NodeList;

import com.fasterxml.jackson.databind.JsonNode;
import com.fasterxml.jackson.databind.ObjectMapper;
import com.fasterxml.jackson.databind.node.ArrayNode;
import com.fasterxml.jackson.databind.node.ObjectNode;

import java.io.File;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;
import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

/**
 * Parses JUnit 5 XML reports from Maven Surefire plugin. Extracts test metrics including
 * counts, pass/fail/skip status, and execution times.
 */
public class JunitReportParser {

	private static final ObjectMapper mapper = new ObjectMapper();

	/**
	 * Parse all JUnit XML reports from surefire-reports directory.
	 * @param reportsDir Path to surefire-reports directory
	 * @return JSON object with aggregated test metrics
	 */
	public ObjectNode parseTestReports(String reportsDir) throws Exception {
		System.out.println(">>> Parsing JUnit reports from: " + reportsDir);

		File dir = new File(reportsDir);
		if (!dir.exists()) {
			System.err.println("Reports directory not found: " + reportsDir);
			return createEmptyReportNode();
		}

		// Find all TEST-*.xml files
		File[] xmlFiles = dir.listFiles((d, name) -> name.startsWith("TEST-") && name.endsWith(".xml"));
		if (xmlFiles == null || xmlFiles.length == 0) {
			System.err.println("No test reports found in: " + reportsDir);
			return createEmptyReportNode();
		}

		// Parse each XML file
		List<ObjectNode> testSuites = new ArrayList<>();
		long totalDuration = 0;

		for (File xmlFile : xmlFiles) {
			try {
				ObjectNode suiteNode = parseTestSuiteXml(xmlFile);
				if (suiteNode != null) {
					testSuites.add(suiteNode);
					if (suiteNode.has("duration")) {
						totalDuration += suiteNode.get("duration").asLong();
					}
				}
			}
			catch (Exception e) {
				System.err.println("Failed to parse report: " + xmlFile.getName() + ", error: " + e.getMessage());
			}
		}

		// Aggregate metrics across all test suites
		return aggregateTestMetrics(testSuites, totalDuration);
	}

	/**
	 * Parse a single JUnit XML test suite file. Format: JUnit 5 XML as generated by
	 * maven-surefire-plugin
	 */
	private ObjectNode parseTestSuiteXml(File xmlFile) throws Exception {
		DocumentBuilderFactory factory = DocumentBuilderFactory.newInstance();
		DocumentBuilder builder = factory.newDocumentBuilder();
		Document doc = builder.parse(xmlFile);

		Element root = doc.getDocumentElement();

		ObjectNode suiteNode = mapper.createObjectNode();
		suiteNode.put("name", root.getAttribute("name"));
		suiteNode.put("package", root.getAttribute("package"));

		// Parse counts
		int tests = parseInt(root.getAttribute("tests"));
		int failures = parseInt(root.getAttribute("failures"));
		int errors = parseInt(root.getAttribute("errors"));
		int skipped = parseInt(root.getAttribute("skipped"));
		double time = parseDouble(root.getAttribute("time"));

		suiteNode.put("total_tests", tests);
		suiteNode.put("passed", tests - failures - errors - skipped);
		suiteNode.put("failed", failures + errors);
		suiteNode.put("skipped", skipped);
		suiteNode.put("duration", (long) (time * 1000)); // Convert to milliseconds

		// Parse individual test cases
		ArrayNode testCases = mapper.createArrayNode();
		NodeList testcaseElements = root.getElementsByTagName("testcase");

		for (int i = 0; i < testcaseElements.getLength(); i++) {
			Element testElement = (Element) testcaseElements.item(i);
			ObjectNode testNode = parseTestCase(testElement);
			if (testNode != null) {
				testCases.add(testNode);
			}
		}

		suiteNode.set("test_cases", testCases);

		return suiteNode;
	}

	/**
	 * Parse individual test case element.
	 */
	private ObjectNode parseTestCase(Element testElement) {
		ObjectNode testNode = mapper.createObjectNode();

		String classname = testElement.getAttribute("classname");
		String name = testElement.getAttribute("name");
		double time = parseDouble(testElement.getAttribute("time"));

		testNode.put("class", classname);
		testNode.put("method", name);
		testNode.put("duration_ms", (long) (time * 1000));

		// Check for failure, error, or skipped
		NodeList failureNodes = testElement.getElementsByTagName("failure");
		NodeList errorNodes = testElement.getElementsByTagName("error");
		NodeList skippedNodes = testElement.getElementsByTagName("skipped");

		if (failureNodes.getLength() > 0) {
			testNode.put("status", "FAILED");
			Element failure = (Element) failureNodes.item(0);
			testNode.put("error_message", failure.getAttribute("message"));
			testNode.put("error_type", failure.getAttribute("type"));
		}
		else if (errorNodes.getLength() > 0) {
			testNode.put("status", "ERROR");
			Element error = (Element) errorNodes.item(0);
			testNode.put("error_message", error.getAttribute("message"));
			testNode.put("error_type", error.getAttribute("type"));
		}
		else if (skippedNodes.getLength() > 0) {
			testNode.put("status", "SKIPPED");
		}
		else {
			testNode.put("status", "PASSED");
		}

		return testNode;
	}

	/**
	 * Aggregate metrics across all test suites.
	 */
	private ObjectNode aggregateTestMetrics(List<ObjectNode> testSuites, long totalDuration) {
		ObjectNode result = mapper.createObjectNode();

		int totalTests = 0;
		int totalPassed = 0;
		int totalFailed = 0;
		int totalSkipped = 0;
		List<ObjectNode> allFailedTests = new ArrayList<>();

		for (ObjectNode suite : testSuites) {
			totalTests += suite.get("total_tests").asInt();
			totalPassed += suite.get("passed").asInt();
			totalFailed += suite.get("failed").asInt();
			totalSkipped += suite.get("skipped").asInt();

			// Collect failed tests
			ArrayNode testCases = (ArrayNode) suite.get("test_cases");
			if (testCases != null) {
				for (JsonNode testCase : testCases) {
					if ("FAILED".equals(testCase.get("status").asText())
							|| "ERROR".equals(testCase.get("status").asText())) {
						allFailedTests.add((ObjectNode) testCase);
					}
				}
			}
		}

		// Build aggregated result
		result.put("total_tests", totalTests);
		result.put("passed", totalPassed);
		result.put("failed", totalFailed);
		result.put("skipped", totalSkipped);
		result.put("duration_ms", totalDuration);

		// Calculate pass rate
		if (totalTests > 0) {
			double passRate = ((double) totalPassed / totalTests) * 100.0;
			result.put("pass_rate", Math.round(passRate * 100.0) / 100.0);
		}
		else {
			result.put("pass_rate", 0.0);
		}

		// Add failed tests list
		ArrayNode failedTestsArray = mapper.createArrayNode();
		for (ObjectNode failedTest : allFailedTests) {
			failedTestsArray.add(failedTest);
		}
		result.set("failed_tests", failedTestsArray);

		// Add test suites
		ArrayNode suitesArray = mapper.createArrayNode();
		for (ObjectNode suite : testSuites) {
			suitesArray.add(suite);
		}
		result.set("test_suites", suitesArray);

		return result;
	}

	/**
	 * Create empty report node when no reports found.
	 */
	private ObjectNode createEmptyReportNode() {
		ObjectNode node = mapper.createObjectNode();
		node.put("total_tests", 0);
		node.put("passed", 0);
		node.put("failed", 0);
		node.put("skipped", 0);
		node.put("duration_ms", 0);
		node.put("pass_rate", 0.0);
		node.set("failed_tests", mapper.createArrayNode());
		node.set("test_suites", mapper.createArrayNode());
		return node;
	}

	/**
	 * Parse integer, default to 0 on error.
	 */
	private int parseInt(String value) {
		try {
			return Integer.parseInt(value);
		}
		catch (NumberFormatException e) {
			return 0;
		}
	}

	/**
	 * Parse double, default to 0.0 on error.
	 */
	private double parseDouble(String value) {
		try {
			return Double.parseDouble(value);
		}
		catch (NumberFormatException e) {
			return 0.0;
		}
	}

}
